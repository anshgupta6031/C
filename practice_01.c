//          Program to calculate area of a rectangle



#include <stdio.h>

int main(){
    float a,b;
    printf("Enter the first side of rectangle\n");
    scanf("%f",&a);
    printf("Enter the second side of rectangle\n");
    scanf("%f",&b);
    printf("The area of rectangle is %f \n",a*b);

    return 0;
}
