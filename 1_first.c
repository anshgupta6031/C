#include <stdio.h>
/*
this is just to show
how we can write a comment
in a paragraph form.
It is called multi line comment
*/
int main()
{
    int a = 6;
    float b = 8.5356;
    char c = 'p'; // char only stores one character.
    int d = 50;
    printf("The value of a is %d\n", a);
    printf("The value of b is %f\n", b);
    printf("The value of c is %c\n", c);
    printf("Sum of a and d is %f\n", a + b*d);
    return 0;
}